int		ft_count_if(char **tab, int (*f)(char*))
{
	int		counter;
	int		n;

	n = 0;
	counter = 0;
	while (tab[n])
	{
		if (f(tab[n]) == 1)
			counter++;
		n++;
	}
	return (counter);
}
